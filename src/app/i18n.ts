export type TranslationKey = keyof typeof en;

export const translate = (locale: Locale, key: TranslationKey): string =>
  locale === "pt-BR" ? pt[key] : en[key];

export const useI18n = () => {
  const { state, setLocale } = useProgress();
  const t = useCallback(
    (key: TranslationKey) => translate(state.locale, key),
    [state.locale]
  );
  return { locale: state.locale, setLocale, t };
};
